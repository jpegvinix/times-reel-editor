const buildPlaceholderShield = (name: string) => {
  const initials = name
    .split(/[\s-]+/)
    .filter(Boolean)
    .slice(0, 2)
    .map((part) => part[0]?.toUpperCase() ?? '')
    .join('')

  const svg = `
    <svg xmlns="http://www.w3.org/2000/svg" width="240" height="240" viewBox="0 0 240 240">
      <defs>
        <linearGradient id="bg" x1="0%" y1="0%" x2="100%" y2="100%">
          <stop offset="0%" stop-color="#0f172a" />
          <stop offset="100%" stop-color="#2563eb" />
        </linearGradient>
      </defs>
      <rect width="240" height="240" rx="48" fill="url(#bg)" />
      <circle cx="120" cy="88" r="42" fill="rgba(255,255,255,0.12)" />
      <text x="120" y="145" text-anchor="middle" font-family="Inter, Arial, sans-serif" font-size="64" font-weight="700" fill="#ffffff">${initials}</text>
      <text x="120" y="194" text-anchor="middle" font-family="Inter, Arial, sans-serif" font-size="16" fill="rgba(255,255,255,0.72)">ESCUDO PENDENTE</text>
    </svg>
  `

  return `data:image/svg+xml;charset=UTF-8,${encodeURIComponent(svg)}`
}

